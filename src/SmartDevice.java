public class SmartDevice {

    String marca;
    String modelo;
    String sistemaOperativo;
    String conexion;
    String red;
    String pantalla;


    public SmartDevice() {
    }

    public SmartDevice(String marca, String modelo, String sistemaOperativo, String conexion, String red, String pantalla) {
        this.marca = marca;
        this.modelo = modelo;
        this.sistemaOperativo = sistemaOperativo;
        this.conexion = conexion;
        this.red = red;
        this.pantalla = pantalla;
    }


}




